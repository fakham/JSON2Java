package com.company;

public abstract class BaseClass {
    private String name;
    private String type;

    public BaseClass() {

    }

    public BaseClass(String name, String type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
